#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a,b,c;
    a = 10;
    b = 5;

    //SOMA
    c = a+b;
    printf("a + b = %d\n",c);

    //SUBSTRAÇÃO
    c = a-b;
    printf("a - b = %d\n",c);

    //MULTIPLICAÇÃO
    c = a*b;
    printf("a * b = %d\n",c);

    //DIVISÃO
    c = a/b;
    printf("a / b = %d\n",c);

}
